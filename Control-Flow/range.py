# 0 to 4 print numbers using range function
print("\n0 to 4 numbers:")
for i in range(5):
    print(i)
    
# 1 to 5 print numbers using range function 
print("\n1 to 5 numbers:")
for i in range(1, 6):
    print(i)
    
# 0 to 10 print even numbers using range function
print("\nEven numbers from 0 to 10:")
for i in range(0, 11, 2):
    print(i)

# sum of numbers from 0 to 5 using range function
total = sum(range(6))  # 0 + 1 + 2 + 3 + 4 + 5
print("\nSum of numbers from 0 to 5:", total)

